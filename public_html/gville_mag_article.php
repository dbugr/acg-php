						<br>
						This month, she's organized a four-day barefoot Windjammer cruise to the
						Bahamas for club members who want to get their feet wet.<br>
						<br>
						Segal joined the club five years ago after a divorce, &quot;to be more active and
						do fun things with other people.&quot;<br>
						<br>
