          <img border="0" src="images/article2_lg.jpg" align="right" alt="Members gather at the monthly Meet n' Greet, May 2008" width="339" height="247">Online social clubs prove popular</h3>
        <p><i>Article excerpt reprinted from the <b>Gainesville Sun</b></i><br>
          <br>
          By MEGAN ROLLAND<br>
          Sun staff writer<br>
          <br>
          Published: Monday, May 26, 2008 <br>
          <br>
          There's Linkedin.com for professional networking, Meetup.com for
          friendships, UrbanChat.com for high school kids and Match.com for the
          romantically inclined.<br>
          <br>
          As the technology era explodes, friendship and relationship networks have
          increasingly turned to online forums, eliminating the days of phone tag and
          friend-of-a-friend in-person introductions.<br>
          <br>
          Nowhere is this phenomena seen more than among high school and college
          students who use social networks like Facebook.com and Myspace.com somewhat
          compulsively.<br>
          <br>
          A recent book co-authored by a University of Florida assistant vice
          president for student affairs takes an in-depth look at the &quot;Net
          Generation,&quot; those born since 1982, and how online interaction and
          friendship affects their values and communication.<br>
          <br>
          However, the middle-aged in Gainesville are stepping into the cyber world,
          as well, something evidenced by perusing online social clubs.<br>
          <br>
          &quot;People are waiting longer to get married, or they find themselves single at
          a later age because of divorce,&quot; said University of Florida sociologist
          Tanya Koropeckyj-Cox.<br>
          <br>
          &quot;These are people who maybe don't have the time or energy to be going to
          bars or the kind of things that young people do to meet people,&quot; Koropeckyj-Cox
          said. &quot;(The online social networks) become a more efficient way to meet
          people with like interests so you're not wasting your time.&quot;<br>
          <br>
          The Adventure Club of Gainesville is perhaps the most widely used local
          social network. For $15 a month members have access to a Web site where
          other members post outdoor or activity-based events and social gatherings.<br>
          <br>
          &quot;The events change every six hours,&quot; said Jere Steele, president and owner
          of the club. &quot;There's no way without the Internet that it could exist.&quot;<br>
          <br>
          Steele said there are currently 100 members in the club with an average age
          of 43. While 75 percent of the members are single, Steele emphasized it is
          not a &quot;singles club.&quot;<br>
          <br>
          Koropeckyj-Cox said that there has been a boom in online social networks and
          subsequently many studies by sociologists.<br>
          <br>
          &quot;Societies have always found ways to match people up, and we have always
          used our networks to do that,&quot; Koropeckyj-Cox said. &quot;Technology uses the
          same networks, it just expands it.&quot;<br>
          <br>
          &quot;We tend not to date perfect strangers. We go through a friend of a friend.
          Technology widens the pool. It also does simplify some of the screening
          process,&quot; she said.<br>
          <br>
          Steele said he screens members of the group by simply not renewing the
          memberships of those who act inappropriately.<br>
          <br>
          &quot;We've had a couple people come into the group that I've had to talk to
          because they were too aggressive or assertive,&quot; Steele said. &quot;Or there's a
          drama king or queen right in the middle of something stirring things up. We
          ask them to leave politely, or I can just not renew their membership.&quot;<br>
          <br>
          Steele said that bad situations are the anomaly in his group however, which
          he said gets along beautifully.<br>
          <br>
          The club focuses on events like kayaking, hiking, biking or ski trips,
          Steele said. However, social dinners are regularly organized and hosted
          through the Web site.<br>
